fun1<-function(n11,n12,n13,n21,n22,n23,n31,n32,n33,v11,v12,v13,v21,v22,v23,v31,v32,v33)
{
  g11<-rnorm(n11,0,sqrt(v11));g12<-rnorm(n12,0,sqrt(v12));g13<-rnorm(n13,0,sqrt(v13))
  g21<-rnorm(n21,0,sqrt(v21));g22<-rnorm(n22,0,sqrt(v22));g23<-rnorm(n23,0,sqrt(v23))
  g31<-rnorm(n31,0,sqrt(v31));g32<-rnorm(n32,0,sqrt(v32));g33<-rnorm(n33,0,sqrt(v33))
  s11=var(g11);s12=var(g12);s13=var(g13);s21=var(g21);s22=var(g22);s23=var(g23);s31=var(g31);s32=var(g32);s33=var(g33)
  S1=(s11/n11+s21/n21);S2=(s22/n22+s12/n12);S3=(s23/n23+s13/n13);S4=(s21/n21+s31/n31);S5=(s22/n22+s32/n32);S6=(s23/n23+s33/n33)
  X11=mean(g11);X12=mean(g12);X13=mean(g13);X21=mean(g21);X22=mean(g22);X23=mean(g23)
  X31=mean(g31);X32=mean(g32);X33=mean(g33)
  T11=(X21-X11)/sqrt(S1);T12=(X22-X12)/sqrt(S2);T13=(X23-X13)/sqrt(S3);T21=(X31-X21)/sqrt(S4);T22=(X32-X22)/sqrt(S5);T23=(X33-X23)/sqrt(S6)
  # max-T value
  T.1=max(T11,T21);T.2=max(T12,T22);T.3=max(T13,T23)
  TNM=min(T.1,T.2,T.3,na.rm = FALSE)
  return(TNM)
}
