fun2<-function(n11,n12,n21,n22,n31,n32,v11,v12,v21,v22,v31,v32)
{
  x=replicate(1000,fun1(n11,n12,n21,n22,n31,n32,v11,v12,v21,v22,v31,v32))
  y<-sort(x,decreasing=FALSE)
  c<-y[50]
  return(c)
}

